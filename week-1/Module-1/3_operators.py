# Arithmetic operators

num1 = 45
num2 = 5
print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 // num2) # Floor Division
print(num1 % num2) 
print( 3 ** 5)

# Comparison Operators

x = 10
y = 3

print(x > y)  # Greater than
print(x < y)  # Less than
print(x == y) # Equal to
print(x != y) # Not equal to
print(x >= y) # Greater than or equal to
print(x <= y) # Less than or equal to




